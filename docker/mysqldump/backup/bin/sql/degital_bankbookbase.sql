/*
* テーブル[userdetail,memo]の作成
* 既に存在すれば作成しない
*/
CREATE TABLE IF NOT EXISTS userdetail(
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  user_id CHAR(32) NOT NULL, 
  child_user_id CHAR(32) NOT NULL,
  insert_datetime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_datetime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY(id),
  UNIQUE KEY _uniqekey (user_id,child_user_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS memo(
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  user_id CHAR(32) NOT NULL,
  account_id CHAR(20) NOT NULL,
  transaction_id CHAR(20) NOT NULL,
  memo VARCHAR(40),
  insert_datetime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_datetime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY(id),
  UNIQUE KEY _uniqekey (account_id,transaction_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;


